                e.preventDefault();
                console.log('kliked ');
                var filternyakak = $("#filternyakak").val();

                if (filternyakak=="" || filternyakak=="all") {
                    // console.log("kosong");
                    $(".filter:hidden").slice(0, 3).slideDown();
                    var img_hide = $('.filter:hidden');
                    if(img_hide.length == 0) {
                        $(".loadMore").css("display", "none");
                        $(".hilang").css("display", "block");
                        console.log('max');
                    }
                }else{
                    // console.log("load kak "+filternyakak);
                    $(".filter:hidden").filter('.'+filternyakak).slice(0, 3).slideDown();
                    
                    var x, i;
                    x = document.querySelectorAll('.filter:not(.'+filternyakak+')');
                    for (i = 0; i < x.length; i++) {
                        x[i].style.display = "none";
                    }
        
                    var img_hide = $('.filter:hidden').filter('.'+filternyakak);
                    if(img_hide.length == 0) {
                        $(".loadMore").css("display", "none");
                        $(".hilang").css("display", "block");
                        console.log('max');
                    }
                }
    
            });

